if z=='Текст' or z=='текст':
    x=float(input('Какой занимает текст в МБ'))
    a=x*(2**23)*i3/UTF_16/10**6/6
    a=float('{:.2f}'.format(a))
    print(round(a,1),'МТ (мегатрайтов) Столько будет занимать в памяти компьютера. Занимает места на компьютере примерно также, как', round(x/UTF_16*i3,2), 'МБ')
elif z=='Фото' or z=='фото':
    x=float(input('Сколько весит Ваше фото в МБ?'))
    a=x*(2**23)*N3/N/10**6/6
